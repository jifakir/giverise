const CountrySelect = ({ onChange, value, label = 'What is your country of origin?', isFloatingLabel = false, }: { onChange?: (value: string) => void, value?: string, label?: string, isFloatingLabel?: boolean }) => {
    const [filteredCountries, setFilteredCountries] = useState(CountryList);
    const [searchKeyword, setSearchKeyword] = useState('');


    useEffect(() => {
        if (!searchKeyword.trim().length) {
            setFilteredCountries([...CountryList]);
        } else {
            setFilteredCountries([...CountryList.filter(country => country.toLowerCase().includes(searchKeyword))]);
        }
    }, [searchKeyword]);

    return (
        <div className="mb-4 relative w-full">
            <label htmlFor="firstName" className={`block text-primary font-medium ${isFloatingLabel ? "absolute top-2 left-3 z-10 text-xs" : "mb-2  text-sm"}`}>
                {label}
            </label>
            <Select
                onChange={onChange}
                defaultValue={value}
                dropdownClassName='rounded-lg'
                className={`w-full custom-select ${isFloatingLabel ? 'floating-label' : ''}`}
                placeholder='Select'
                dropdownRender={menu => {
                    return (
                        <div className='p-4 rounded-lg custom-renderer'>
                            <div className="relative w-full mb-4">
                                <input value={searchKeyword} onChange={e => setSearchKeyword(e.target.value)} placeholder='Search' className='text-xs text-body w-full rounded-lg h-7 px-8 border border-[#E2E4E8] box-border focus:outline-none' />
                                <span className='absolute top-2 left-3 text-body text-sm'>
                                    <BiSearch />
                                </span>

                                <button className='absolute top-2 right-3 text-body text-sm' onClick={() => setSearchKeyword('')}>
                                    <BiXCircle />
                                </button>
                            </div>
                            {menu}
                        </div>
                    );
                }}
            >
                {filteredCountries.map(country => (
                    <Select.Option key={country} value={country}>{country}</Select.Option>
                ))}
            </Select>
        </div>
    )
}

export default CountrySelect;